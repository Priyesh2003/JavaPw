public class Array {
    public static void main(String []args)
    {
        int nums[][]={{2,7},{9,3},{5,6}};
        for (int i=0;i<=2;i++)
        {
            for (int j=0;j<=1;j++)
            {
                System.out.print(nums[i][j]+ " ");
            }
            System.out.println();
        }
    }
}
